function normalizeBrandWithStrength(active, brand) {
  var activeText = String(active || '').trim();
  var brandText = String(brand || activeText).trim();
  var strengthMatch = activeText.match(/(\d+(?:[.,]\d+)?\s*(?:mg|mcg|g|ml|ui|%)(?:\s*\/\s*\d+(?:[.,]\d+)?\s*(?:mg|mcg|g|ml|ui|%))?)/i);
  if (!strengthMatch) return brandText;
  var strength = strengthMatch[1].replace(/\s+/g, ' ').trim();
  if (new RegExp(strength.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'), 'i').test(brandText)) return brandText;
  if (/\d/.test(brandText)) return brandText;
  return `${brandText} ${strength}`.trim();
}
function normalizeUnit(active, brand, usage, unit) {
  var hay = `${active || ''} ${brand || ''} ${usage || ''}`.toLowerCase();
  if (/goi|gói|sachet|powder|bot pha|cốm/.test(hay)) return 'Gói';
  if (/500ml|250ml|chai|dich truyen|dịch truyền|glucose|ringer|natri clorid/.test(hay)) return 'Chai';
  if (/\/\d+ml|mg\/ml|\bim\b|\biv\b|tiem|tiêm|tinh mach|tĩnh mạch|bap|bắp|ampoule|ong|ống/.test(hay)) return 'Ống';
  if (/vien|viên|tablet|tabs|tab\b|xr\b|mg\b|mcg\b/.test(hay)) return 'Viên';
  var cleanUnit = String(unit || '').trim().toLowerCase();
  if (cleanUnit.includes('goi') || cleanUnit.includes('gói')) return 'Gói';
  if (cleanUnit.includes('chai')) return 'Chai';
  if (cleanUnit.includes('ong') || cleanUnit.includes('ống')) return 'Ống';
  return 'Viên';
}
