(function(){
  try {
    var key = 'pms_icd_v6';
    var imported = Array.isArray(window.ICD_IMPORT) ? window.ICD_IMPORT : [];
    var stored = [];
    try { stored = JSON.parse(localStorage.getItem(key) || '[]'); } catch (e) { stored = []; }
    var merged = [];
    var seen = new Set();
    imported.concat(Array.isArray(stored) ? stored : []).forEach(function(item){
      if (!item || !item.code || !item.name) return;
      var code = String(item.code).trim();
      var name = String(item.name).trim();
      var normalized = code.toLowerCase();
      if (seen.has(normalized)) return;
      seen.add(normalized);
      merged.push({ code: code, name: name });
    });
    localStorage.setItem(key, JSON.stringify(merged));
  } catch (e) {
    console.error('ICD import failed', e);
  }
})();
